                    <p>Before the widespread adoption of renewable energy, several issues were associated with traditional,
                         non-renewable energy sources. Some of the key problems include</p>
                    <p>Environmental Impact: Fossil fuel-based energy sources, such as coal, oil, and natural gas,
                         contribute to air and water pollution, deforestation, and habitat destruction.
                         The extraction, transportation, and combustion of these fuels release greenhouse gases,
                          contributing to climate change.</p>
                          <p>Resource Depletion: Non-renewable resources like coal and oil are finite, leading to concerns about resource depletion.
                             As these resources are used, their availability decreases, and extraction becomes more challenging.</p>
                             <p>Energy Security: Dependency on fossil fuels, often imported from politically unstable regions, posed a risk to energy security.
                                 Fluctuations in oil prices and geopolitical tensions could impact the stability of energy supplies.</p>	

                                 <p>Economic Costs: Traditional energy sources involve significant economic costs, including exploration, extraction, and transportation. 
                                    The volatility of fossil fuel prices can affect energy affordability and economic stability.</p>
                    
